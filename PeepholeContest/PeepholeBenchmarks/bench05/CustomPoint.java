public class CustomPoint {

    protected int x;
    protected int y;


    public CustomPoint() {
        super();

        x = 0;
        y = 0;
    }


    public CustomPoint(int x1, int y1) {
        super();

        x = x1;
        y = y1;
    }


    public int getX() {
        return x;
    }


    public int getY() {
        return y;
    }
}
